////////////////////////////////////////////////////////////////////////////////

void GfConstantBuffer::BindBuffer(const GfBuffer::GfRange& kRange)
{
	if (!m_uiFlags.IsEnable(EFlags::BufferBound))
	{
		m_kBufferRange = kRange;
		m_uiFlags |= (EFlags::BufferBound | EFlags::GPUDirty);
	}
}


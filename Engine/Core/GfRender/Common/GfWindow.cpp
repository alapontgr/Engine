#include "GfRender/Common/GfWindow.h"

////////////////////////////////////////////////////////////////////////////////

GfWindow::GfWindow()
	: GfWindow_Platform(*this)
	, m_szAppName(nullptr)
	, m_uiWidth(0)
	, m_uiHeight(0)
	, m_bFullScreen(false)
	, m_bVSync(false)
{
}

////////////////////////////////////////////////////////////////////////////////

void GfWindow::Init(GfWindowInitParams& kInitParams)
{
	m_szAppName = kInitParams.m_szWindowName;
	m_uiWidth = kInitParams.m_uiWidth;
	m_uiHeight = kInitParams.m_uiHeight;
	m_bFullScreen = kInitParams.m_bFullScreen;
	m_bVSync = kInitParams.m_bVSync;
	InitPlatform(kInitParams);
}

////////////////////////////////////////////////////////////////////////////////

Bool GfWindow::Tick()
{
	return TickPlatform();
}

////////////////////////////////////////////////////////////////////////////////

void GfWindow::Shutdown()
{
	ShutdownPlatform();
}

